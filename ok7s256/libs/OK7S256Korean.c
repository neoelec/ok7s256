unsigned int cursor_flag = 0;

/* ------------------------------------------------------------------------------- */
/*    Write a Command                                                              */
/* ------------------------------------------------------------------------------- */
void GLCD_command(unsigned int cs, unsigned int command)
{
  AT91F_PIO_ClearOutput(AT91C_BASE_PIOA, GLCD_CS2 | GLCD_CS1);  // CS0 = CS1 = 0
  AT91F_PIO_SetOutput(AT91C_BASE_PIOA, cs);    // CS0 or CS1 = 1
  AT91F_PIO_ClearOutput(AT91C_BASE_PIOA, GLCD_DI);  // D/-I = 0
  Delay_1us();

  AT91F_PIO_SetOutput(AT91C_BASE_PIOA, command << 24);  // output command
  AT91F_PIO_ClearOutput(AT91C_BASE_PIOA, (~command) << 24);
  AT91F_PIO_SetOutput(AT91C_BASE_PIOA, GLCD_E); // E = 1
  Delay_1us();
  AT91F_PIO_ClearOutput(AT91C_BASE_PIOA, GLCD_E); // E = 0

  Delay_us(10);                                // wait for GLCD busy
}

/* ------------------------------------------------------------------------------- */
/*    Write a DD RAM Data                                                          */
/* ------------------------------------------------------------------------------- */
void GLCD_data(unsigned int cs, unsigned int data)
{
  AT91F_PIO_ClearOutput(AT91C_BASE_PIOA, GLCD_CS2 | GLCD_CS1);  // CS0 = CS1 = 0
  AT91F_PIO_SetOutput(AT91C_BASE_PIOA, cs);    // CS0 or CS1 = 1
  AT91F_PIO_SetOutput(AT91C_BASE_PIOA, GLCD_DI);  // D/-I = 1
  Delay_1us();

  AT91F_PIO_SetOutput(AT91C_BASE_PIOA, data << 24); // output data
  AT91F_PIO_ClearOutput(AT91C_BASE_PIOA, (~data) << 24);
  AT91F_PIO_SetOutput(AT91C_BASE_PIOA, GLCD_E); // E = 1
  Delay_1us();
  AT91F_PIO_ClearOutput(AT91C_BASE_PIOA, GLCD_E); // E = 0

  Delay_us(10);                                // wait for GLCD busy
}

/* ------------------------------------------------------------------------------- */
/*    Clear GLCD Screen                                                            */
/* ------------------------------------------------------------------------------- */
void GLCD_clear(void)
{
  unsigned int i, j, x;

  AT91F_PIO_CfgOutput(AT91C_BASE_PIOA, GLCD_ALL); // clear all GLCD control signals
  AT91F_PIO_ClearOutput(AT91C_BASE_PIOA, GLCD_ALL);
  AT91F_PIO_Opendrain(AT91C_BASE_PIOA, GLCD_ALL);

  GLCD_command(GLCD_CS2 | GLCD_CS1, 0x3F);     // CS1,CS2 display ON
  GLCD_command(GLCD_CS2 | GLCD_CS1, 0xC0);     // CS1,CS2 display position

  x = 0xB8;
  for (i = 0; i <= 7; i++) {
    GLCD_command(GLCD_CS2 | GLCD_CS1, x);      // X start addtess
    GLCD_command(GLCD_CS2 | GLCD_CS1, 0x40);   // Y start address
    for (j = 0; j <= 63; j++)
      GLCD_data(GLCD_CS2 | GLCD_CS1, 0x00);    // clear CS1 and CS2 screen
    x++;
  }
}

/* ------------------------------------------------------------------------------- */
/*    Set English Character Position                                               */
/* ------------------------------------------------------------------------------- */
void GLCD_xy(unsigned int x, unsigned int y)
{
  xcharacter = x;                              // x = 0 - 3
  ycharacter = y;                              // y = 0 - 15
}

/* ------------------------------------------------------------------------------- */
/*    Set Character Upper/Lower Row                                                */
/* ------------------------------------------------------------------------------- */
void GLCD_xy_row(unsigned int x, unsigned int y, unsigned int row)
{
  if (row == 0)                                // if upper row, set Y address
    GLCD_command(GLCD_CS2 | GLCD_CS1, 0xB8 + x * 2);
  else                                         // if lower row, set Y address + 1
    GLCD_command(GLCD_CS2 | GLCD_CS1, 0xB8 + x * 2 + 1);

  if (y <= 7)                                  // if y <= 7, CS1 Y address
    GLCD_command(GLCD_CS1, 0x40 + y * 8);
  else                                         // if y >= 8, CS2 Y address
    GLCD_command(GLCD_CS2, 0x40 + (y - 8) * 8);
}

/* ------------------------------------------------------------------------------- */
/*    Display a 8x16 Dot English(ASCII)                                            */
/* ------------------------------------------------------------------------------- */
void GLCD_English(unsigned int attribute, unsigned char Ecode)
{
  unsigned int i, cs, x;

  GLCD_xy_row(xcharacter, ycharacter, 0);      // display upper row
  if (ycharacter <= 7)                         // if y <= 7, CS1
    cs = GLCD_CS1;
  else                                         // if y >= 8, CS2
    cs = GLCD_CS2;
  for (i = 0; i <= 7; i++) {
    x = E_font[Ecode][i];
    if (attribute & 0x01)                      // reverse ?
      x = ~x;
    GLCD_data(cs, x);
  }

  GLCD_xy_row(xcharacter, ycharacter, 1);      // display lower row
  if (ycharacter <= 7)                         // if y <= 7, CS1
    cs = GLCD_CS1;
  else                                         // if y >= 8, CS2
    cs = GLCD_CS2;
  for (i = 8; i <= 15; i++) {
    x = E_font[Ecode][i];                      // cursor ?
    if ((cursor_flag == 1) && (xcharacter == xcursor) && (ycharacter == ycursor))
      x |= 0xC0;
    if (attribute & 0x02)                      // underline ?
      x |= 0x80;
    if (attribute & 0x01)                      // reverse ?
      x = ~x;
    GLCD_data(cs, x);
  }

  ycharacter++;                                // go next character position
  if (ycharacter == 16) {
    ycharacter = 0;
    xcharacter++;
  }
}

/* ------------------------------------------------------------------------------- */
/*    Display a 16x16 Dot Korean                                                   */
/* ------------------------------------------------------------------------------- */
void GLCD_Korean(unsigned int attribute, unsigned int Kcode)
{
  unsigned int i, cs, x, character;
  unsigned int cho_5bit, joong_5bit, jong_5bit;
  unsigned int cho_bul, joong_bul, jong_bul = 0, jong_flag;
  unsigned char Korean_buffer[32];             // 32 byte Korean font buffer

  if (ycharacter == 15)                        // if can not display, go next line
    GLCD_English(0x20, 0);

  cho_5bit = table_cho[(Kcode >> 10) & 0x001F]; // get cho, joong, jong 5 bit
  joong_5bit = table_joong[(Kcode >> 5) & 0x001F];
  jong_5bit = table_jong[Kcode & 0x001F];

  if (jong_5bit == 0) {                        // don't have jongsung
    jong_flag = 0;
    cho_bul = bul_cho1[joong_5bit];
    if ((cho_5bit == 1) || (cho_5bit == 16))
      joong_bul = 0;
    else
      joong_bul = 1;
  } else {                                     // have jongsung
    jong_flag = 1;
    cho_bul = bul_cho2[joong_5bit];
    if ((cho_5bit == 1) || (cho_5bit == 16))
      joong_bul = 2;
    else
      joong_bul = 3;
    jong_bul = bul_jong[joong_5bit];
  }

  character = cho_bul * 20 + cho_5bit;         // copy chosung font
  for (i = 0; i <= 31; i++)
    Korean_buffer[i] = K_font[character][i];

  character = 8 * 20 + joong_bul * 22 + joong_5bit; // OR joongsung font
  for (i = 0; i <= 31; i++)
    Korean_buffer[i] |= K_font[character][i];

  if (jong_flag == 1) {                        // if jongsung, OR jongsung font
    character = 8 * 20 + 4 * 22 + jong_bul * 28 + jong_5bit;
    for (i = 0; i <= 31; i++)
      Korean_buffer[i] |= K_font[character][i];
  }

  GLCD_xy_row(xcharacter, ycharacter, 0);      // display upper left row
  if (ycharacter <= 7)                         // if y <= 7, CS1
    cs = GLCD_CS1;
  else                                         // if y >= 8, CS2
    cs = GLCD_CS2;
  for (i = 0; i <= 7; i++) {
    x = Korean_buffer[i];
    if (attribute & 0x01)                      // reverse ?
      x = ~x;
    GLCD_data(cs, x);
  }

  GLCD_xy_row(xcharacter, ycharacter, 1);      // display lower left row
  if (ycharacter <= 7)                         // if y <= 7, CS1
    cs = GLCD_CS1;
  else                                         // if y >= 8, CS2
    cs = GLCD_CS2;
  for (i = 16; i <= 23; i++) {
    x = Korean_buffer[i];                      // cursor ?
    if ((cursor_flag == 1) && (xcharacter == xcursor) && (ycharacter == ycursor))
      x |= 0xC0;
    if (attribute & 0x02)                      // underline ?
      x |= 0x80;
    if (attribute & 0x01)                      // reverse ?
      x = ~x;
    GLCD_data(cs, x);
  }

  ycharacter++;

  GLCD_xy_row(xcharacter, ycharacter, 0);      // display upper right row
  if (ycharacter <= 7)                         // if y <= 7, CS1
    cs = GLCD_CS1;
  else                                         // if y >= 8, CS2
    cs = GLCD_CS2;
  for (i = 8; i <= 15; i++) {
    x = Korean_buffer[i];
    if (attribute & 0x01)                      // reverse ?
      x = ~x;
    GLCD_data(cs, x);
  }

  GLCD_xy_row(xcharacter, ycharacter, 1);      // display lower right row
  if (ycharacter <= 7)                         // if y <= 7, CS1
    cs = GLCD_CS1;
  else                                         // if y >= 8, CS2
    cs = GLCD_CS2;
  for (i = 24; i <= 31; i++) {
    x = Korean_buffer[i];                      // cursor ?
    if ((cursor_flag == 1) && (xcharacter == xcursor) && (ycharacter == (ycursor + 1)))
      x |= 0xC0;
    if (attribute & 0x02)                      // underline ?
      x |= 0x80;
    if (attribute & 0x01)                      // reverse ?
      x = ~x;
    GLCD_data(cs, x);
  }

  ycharacter++;                                // go next character position
  if (ycharacter == 16) {
    ycharacter = 0;
    xcharacter++;
  }
}

/* ------------------------------------------------------------------------------- */
/*   Display a String(attribute : 0x00=normal, 0x01 = reverse, 0x02 = underline)   */
/* ------------------------------------------------------------------------------- */
void GLCD_string(unsigned int x, unsigned int y, unsigned int attribute, char *string)
{
  uint16_t unicode;
  uint16_t johab;
  uint16_t kcode;
  int len;
  int err;

  GLCD_xy(x, y);                               // x = 0 - 3, y = 0 - 15

  while (*string != '\0') {
    unicode = utf8_to_unicode(string, &len);

    if (len == 1) {
      GLCD_English(attribute, (uint8_t) (unicode & 0xFF));
    } else if (len == 2) {
      GLCD_English(attribute, '#');
    } else if (len == 3) {
      johab = unicode_to_johab(unicode, &err);
      if (err < 0)
        GLCD_English(attribute, '#');
      else {
        kcode = johab >> 8;
        kcode |= (johab & 0xFF) << 8;
        GLCD_Korean(attribute, kcode);
      }
    } else {
      len = 1;
      GLCD_English(attribute, '#');
    }

    string = &string[len];
  }
}
